<h1><img border=0 src="/openqrm/base/plugins/highavailability/img/plugin.png"> High-Availability plugin</h1>
<br>
The highavailability-plugin automatically provides high-availability for the appliances managed by openQRM.
<br>
<br>
<b>How to use :</b>
<br>
<br>
Simply use the HA-Manager to select the appliances which should be high-available.
 In case of an error openQRM will try to find a new resource fitting to the appliance profile and re-start/re-deploy the appliance.
